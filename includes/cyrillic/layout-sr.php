	<p>&nbsp;</p>
	
	<div class="multicolumn3">
		
		<p>Наше приповедање о знаменитим Србима у руској историји настављамо причом о знаменитом руском полководцу српског порекла – генералу Михаилу Андрејевичу Милорадовићу. Илија Милорадовић, наследник чувене племићке породице Милорадовићи из Херцеговине, чија је задужбина познати српски манастир Житомислић у околини Мостара, са синовима Михаилом, Гаврилом и Александром, почетком ХVII века одлази у Русију и већ 1711. године добија чин пуковника. Архимандрит Нићифор Дучић наводи веру као разлог за одлазак ове чувене породице у емиграцију: “породица вјером није хтела да преврне па је радије пристала да тужна срца напусти своју Отаџбину”. Скоро два века ова херцеговачка племићка породица заиста је славно служила вери православној, стојећи на бранику руског православног царства.</p>
