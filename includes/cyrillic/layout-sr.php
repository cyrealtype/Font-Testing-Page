	<p style="font-size: 24px;">Творац теорије о цивилизацијама, руски мислилац Данилевски, истицао је штетност и бескорисност насилног искакања из сопственог цивилизацијског типа. По његовим речима, онај ко искочи из свог цивилизацијског типа, у другом цивилизацијском типу може представљати само смешни сурогат. Данас када “демократска већина” тако упорно ствара илузију о благодатима туђе нам цивилизације и њеним “демократским вредностима”, најбољи начин за раскринкавање “лажног добра” јесте промоција истинског добра.</p>
	<p>&nbsp;</p>
	
	<div class="multicolumn3">
		<p>Најбољи начин за раскринкавање шупље приче о благодатима “евроатланских интеграција” са вековним крвницима нашег рода, јесте истинита прича о промислитељским везама са нашим вековним пријатељима и браћом. Те узајамне везе градили су многи велики људи на различитим поприштима борбе за добробит свог народа и Отаџбине. Као што данашњи еуроунијати очито показују све одлике сурогата о којима је писао Данилевски, бројни знаменити Срби у руској историји, као и Руси у српској, својим делима и својим животима показују припадност истој цивилизацији. </p>
